# 9. Celsius to Fahrenheit Temperature Converter
# Write a program that converts Celsius temperatures to Fahrenheit 
# temperatures. The formula is as follows:
# F=(9/5)*C+32
# The program should ask the user to enter a temperature in Celsius, and then 
# display the
# temperature converted to Fahrenheit.
c=float(input("Please enter a temperature in Celsius:"))
f=(9/5)*c+32
print("You enter a temperature in Celsius:",c)
print("The temperature converted to Fahrenheit is:",f)