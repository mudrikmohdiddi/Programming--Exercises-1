# 7. Miles-per-Gallon
# A car’s miles-per-gallon (MPG) can be calculated with the following formula:
# MPG = Miles driven / Gallons of gas used
# Write a program that asks the user for the number of miles driven and the 
# gallons of gas
# used. It should calculate the car’s MPG and display the result.
m=float(input("Please enter the number of miles driven:"))
g=float(input("Please enter the number of gallons of gas:"))
MPG=m/g
print("A car’s miles-per-gallon (MPG) is:",MPG)